export const getDonationsByProject = (projectId: string): Donation[] => {
  return donations.filter((d) => d.projectId === projectId);
};

export const getDonationById = (id: string): Donation | undefined => {
  return donations.find((d) => d.id === id);
};

export const getAnonymousDonations = (projectId: string): Donation[] => {
  return donations.filter((d) => d.projectId === projectId && d.isAnonymous);
};

export const getPublicDonations = (projectId: string): Donation[] => {
  return donations.filter((d) => d.projectId === projectId && !d.isAnonymous);
};

export const calculateTotalDonations = (projectId: string): number => {
  return donations
    .filter((d) => d.projectId === projectId && d.status === 'success')
    .reduce((sum, d) => sum + d.amount, 0);
};

export default donations;
